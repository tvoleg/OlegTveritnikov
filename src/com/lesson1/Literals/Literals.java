package com.lesson1.Literals;

public class Literals {

    public static void main(String[] args) {
        // it's my first comment
        /*
        it's
        my
        second
        comment
         */
        double d1 = 1488.8841; // double литерал
        float f1 = 2234.562f; // float literal
        int a1 = 0X007; // шестнадцатиричный литерал
        int a2 = 777; // десятиричный литерал
        int a3 = 07; // восьмиричный литерал
        int a4 = 0b101; // двоичный литерал
        char a = 'a'; // символьный литерал
        String a6 = "Это строковый литерал";
        boolean a7 = false;

        System.out.println(a);
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        System.out.println(a4);
        System.out.println(a6);
        System.out.println(a7);
        System.out.println(f1);
        System.out.println(d1);
    }
}